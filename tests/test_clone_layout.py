@pytest.mark.asyncio
async def test_clone_add_preserves_other_voices_json_entries(
    tmp_voicemode, stub_wav
):
    voices_dir = tmp_voicemode["voices_dir"]
    pre_existing = {
        "voices": {
            "alice": {
                "ref_audio": "alice.wav",
                "ref_text": "hi",
                "description": "",
            }
        }
    }
    tmp_voicemode["voices_json"].write_text(json.dumps(pre_existing))

    with patch(
        "voice_mode.tools.impressions.profiles._normalise_audio",
        side_effect=_patch_normalise(voices_dir),
    ), patch(
        "voice_mode.tools.impressions.profiles._transcribe_audio",
        return_value="hello world",
    ):
        await clone_add("testfoo", str(stub_wav))

    data = json.loads(tmp_voicemode["voices_json"].read_text())
    assert "alice" in data["voices"]
    assert data["voices"]["alice"]["ref_audio"] == "alice.wav"
    assert "testfoo" in data["voices"]


@pytest.mark.asyncio
async def test_success_message_includes_layout_paths(tmp_voicemode, stub_wav):
    voices_dir = tmp_voicemode["voices_dir"]
    with patch(
        "voice_mode.tools.impressions.profiles._normalise_audio",
        side_effect=_patch_normalise(voices_dir),
    ), patch(
        "voice_mode.tools.impressions.profiles._transcribe_audio",
        return_value="hello world transcript here",
    ):
        result = await clone_add("testfoo", str(stub_wav))

    msg = result["message"]
    assert f"Path: {voices_dir / 'testfoo'}/" in msg
    assert "default.wav" in msg
    assert "mono 24kHz 16-bit PCM" in msg
    assert 'Text: "hello world transcript here' in msg
